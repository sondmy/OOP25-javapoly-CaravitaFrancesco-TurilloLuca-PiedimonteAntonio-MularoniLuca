package it.unibo.javapoly.model.api.player;

/**
 * Represents the various types of tokens available for players in the JavaPoly
 * game.
 * Each enum constant corresponds to a specific visual representation of a
 * player on the board.
 * 
 * @see Token
 */
public enum TokenType {
    CAR,
    HAT,
    DOG,
    SHOE,
    SHIP,
    CAT
}
